#include "libftprintf.h"

void	ft_put_uphex_fd(uintptr_t n, int fd)
{
	if (n >= 16)
		ft_put_uphex_fd(n / 16, fd);
	ft_putchar_fd("0123456789ABCDEF"[n % 16], fd);
}

