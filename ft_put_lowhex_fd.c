#include "libftprintf.h"

void	ft_put_lowhex_fd(uintptr_t n, int fd)
{
	if (n >= 16)
		ft_put_lowhex_fd(n / 16, fd);
	 ft_putchar_fd("0123456789abcdef"[n % 16], fd);
}
